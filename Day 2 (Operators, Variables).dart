void main() {
  // Arithmetic operators
  int a = 5;
  int b = 10;
  int c = 2;
  int d = 13;
  int e = 4;
  int f = 9;
  int g = 45;

  print(a / b);
  print(c % d);
  print(e * f);
  print(g + b);
  print(g - a);
  
  // Relational operators
  print(a == a);
  print(a != a);
  print(g <= g);
  print(a >= b);
  
  // Logical operators
  print(a == a && b != 8);
  print(b == b || c >= b);
  
  // Variables
  int age = 10;
  print(age);
}